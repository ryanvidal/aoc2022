import Foundation

public let day2 = DailyPuzzleBase(solver: Day2Solver(),
                                  testInput: testInputString,
                                  puzzleInput: puzzleInputString)

public class Day2Solver: DailySolver {
    public typealias CalculationInput = [String]

    public func ParseInput(_ input: String) -> CalculationInput {
        return input.components(separatedBy: "\n")
    }

    public func PerformPart1Calculation(_ input: CalculationInput) -> Int? {
        return input.reduce(0, { $0 + roundScore(for: $1) } )
    }

    public func PerformPart2Calculation(_ input: CalculationInput) -> Int? {
        return input.reduce(0, { $0 + roundScorePart2(for: $1) } )
    }

    /// Naive solution
    private func roundScore(for input: String) -> Int {
        let plays = input.split(separator: " ")
        switch plays[0] {
        case "A":
            switch plays[1] {
            case "X":
                return 1 + 3
            case "Y":
                return 2 + 6
            case "Z":
                return 3 + 0
            default:
                fatalError()
            }
        case "B":
            switch plays[1] {
            case "X":
                return 1 + 0
            case "Y":
                return 2 + 3
            case "Z":
                return 3 + 6
            default:
                fatalError()
            }
        case "C":
            switch plays[1] {
            case "X":
                return 1 + 6
            case "Y":
                return 2 + 0
            case "Z":
                return 3 + 3
            default:
                fatalError()
            }
        default:
            fatalError()
        }
    }

    /// Naive Solution Part 2
    private func roundScorePart2(for input: String) -> Int {
        let plays = input.split(separator: " ")

        switch plays[0] {
        case "A":
            switch plays[1] {
            case "X":
                return 3 + 0
            case "Y":
                return 1 + 3
            case "Z":
                return 2 + 6
            default:
                fatalError()
            }
        case "B":
            switch plays[1] {
            case "X":
                return 1 + 0
            case "Y":
                return 2 + 3
            case "Z":
                return 3 + 6
            default:
                fatalError()
            }
        case "C":
            switch plays[1] {
            case "X":
                return 2 + 0
            case "Y":
                return 3 + 3
            case "Z":
                return 1 + 6
            default:
                fatalError()
            }
        default:
            fatalError()
        }
    }
}

