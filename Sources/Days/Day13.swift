public class Day13Solver: DailySolver {
    public typealias CalculationInput = [(PacketContent, PacketContent)]

    public func ParseInput(_ input: String) -> CalculationInput {
        var packets: [(PacketContent, PacketContent)] = []
        let pairs = input.components(separatedBy: "\n\n")
        for pair in pairs {
            let packetStrings = pair.components(separatedBy: "\n")
            if packetStrings.count == 2 {
                packets.append((PacketContent(packetStrings[0])!, PacketContent(packetStrings[1])!))
            }
        }

        return packets
    }

    public func PerformPart1Calculation(_ input: CalculationInput) -> Int? {
        var sum = 0
        for (i, pair) in input.enumerated() {
            if ValueOrder(pair.0, pair.1) == .before {
                sum += i + 1
            }
        }

        return sum
    }

    public func PerformPart2Calculation(_ input: CalculationInput) -> Int? {
        let divider1: PacketContent = .list([.list([.integer(2)])])
        let divider2: PacketContent = .list([.list([.integer(6)])])
        let allPackets = (input.flatMap { [$0.0, $0.1] } + [divider1, divider2])
            .sorted { a, b in
                return ValueOrder(a, b) == .before
            }

        let divider1Index = allPackets.distance(from: allPackets.startIndex, to: allPackets.firstIndex(where: { $0 == divider1 })!) + 1
        let divider2Index = allPackets.distance(from: allPackets.startIndex, to: allPackets.firstIndex(where: { $0 == divider2 })!) + 1

        return divider1Index * divider2Index
    }
}

public enum PacketContent: Equatable {
    case integer(Int)
    case list([PacketContent])

    init?(_ input: String) {
        guard input.count > 0 else { return nil }
        if input.first == "[" {
            var values: [PacketContent] = []
            guard var contents = PacketContent.substringFromBrackets(in: input) else { self = .list([]); return }

            while contents.count > 0 {
                guard contents.first != "[" else {
                    let contentString = String(contents)
                    let (start, end) = PacketContent.findMatchingBracketIndexes(in: contentString)

                    if let value = PacketContent(String(contentString[start...end])) { values.append(value) }

                    contents = contentString.suffix(from: contentString.index(after: end))
                    if contents.first == "," { contents = contents.dropFirst() }
                    continue
                }
                let commaIndex = contents.firstIndex(of: "," )
                guard let commaIndex = commaIndex else {
                    if let value = PacketContent(String(contents)) { values.append(value) }
                    break
                }
                let valueString = contents.prefix(upTo: commaIndex)
                if let value = PacketContent(String(valueString)) { values.append(value) }
                contents = contents.trimmingPrefix(while: { $0 != "," }).dropFirst()
            }

            self = .list(values)
        } else {
            self = .integer(Int(input)!)
        }
    }

    private static func substringFromBrackets(in input: String) -> String.SubSequence? {
        let (start, end) = findMatchingBracketIndexes(in: input)

        return input[input.index(after: start)..<end]
    }

    private static func findMatchingBracketIndexes(in input: String) -> (String.Index, String.Index) {
        var depth = 0
        for (i, c) in input.enumerated() {
            guard i != 0 else { depth += 1; continue }
            switch c {
            case "[": depth += 1
            case "]": depth -= 1
            default: break
            }
            if depth == 0 {
                return (input.startIndex, input.index(input.startIndex, offsetBy: i))
            }
        }
        fatalError()
    }

    public static func == (_ lhs: PacketContent, _ rhs: PacketContent) -> Bool {
        return ValueOrder(lhs, rhs) == .same
    }
}

fileprivate enum ValueOrder {
    case before
    case same
    case after

    init(_ lhs: PacketContent, _ rhs: PacketContent) {
        switch lhs {
        case .integer(let left):
            switch rhs {
            case .integer(let right):
                if left < right { self = .before }
                else if left == right { self = .same }
                else { self = .after }
            case .list(let right):
                self = ValueOrder.isLeftListBeforeRightList(left: [lhs], right: right)
            }
        case .list(let left):
            switch rhs {
            case .integer:
                self = ValueOrder.isLeftListBeforeRightList(left: left, right: [rhs])
            case .list(let right):
                self = ValueOrder.isLeftListBeforeRightList(left: left, right: right)
            }
        }
    }

    private static func isLeftListBeforeRightList(left: [PacketContent], right: [PacketContent]) -> ValueOrder {
        if left.count == 0 && right.count != 0 { return .before }
        if left.count != 0 && right.count == 0 { return .after }
        for (i, l) in left.enumerated() {
            if i >= right.count { return .after }
            let order = ValueOrder(l, right[i])
            switch order {
            case .same:
                continue
            default:
                return order
            }
        }
        if left.count < right.count { return .before }

        return .same
    }
}

